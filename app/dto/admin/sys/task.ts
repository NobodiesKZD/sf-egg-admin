  @Length(2, 50)
  @Expose()
  name: string;

  @IsString()
  @Expose()
  service: string;

  @IsIn([ 0, 1 ])
  @Expose()
  type: number;

  @IsIn([ 0, 1 ])
  @Expose()
  status: number;

  @IsDate()
  @Expose()
  startTime: Date;

  @IsDate()
  @Expose()
  endTime: Date;

  @IsInt()
  @IsOptional()
  @Expose()
  limit: number;

